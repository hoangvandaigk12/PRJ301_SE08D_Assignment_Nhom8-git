package dtos;

import java.io.Serializable;

/**
 *
 * @author AD
 */
public class FlowerDTO implements Serializable {
    private String id, name, description;
    private float price;
    private int quantity;
    private String image;
    private CategoryDTO cate;

    public FlowerDTO() {
    }

    public FlowerDTO(String id, String name, String description, float price, int quantity, String image, CategoryDTO cate) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
        this.quantity = quantity;
        this.image = image;
        this.cate = cate;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public CategoryDTO getType() {
        return cate;
    }

    public void setType(CategoryDTO cate) {
        this.cate = cate;
    }
    
}
